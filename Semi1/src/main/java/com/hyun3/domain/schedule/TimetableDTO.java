package com.hyun3.domain.schedule;

public class TimetableDTO {
	private String timeTable_num;
	private String studyTime;
	private String studyDay;
	private String DT_sub_num;
	
	public String getTimeTable_num() {
		return timeTable_num;
	}
	public void setTimeTable_num(String timeTable_num) {
		this.timeTable_num = timeTable_num;
	}
	public String getStudyTime() {
		return studyTime;
	}
	public void setStudyTime(String studyTime) {
		this.studyTime = studyTime;
	}
	public String getStudyDay() {
		return studyDay;
	}
	public void setStudyDay(String studyDay) {
		this.studyDay = studyDay;
	}
	public String getDT_sub_num() {
		return DT_sub_num;
	}
	public void setDT_sub_num(String dT_sub_num) {
		DT_sub_num = dT_sub_num;
	}

	
}
